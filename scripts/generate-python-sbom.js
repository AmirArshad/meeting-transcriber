#!/usr/bin/env node
/**
 * Generate a pinned list of Python packages bundled in installers.
 * Does not modify application code. Run from repo root:
 *   node scripts/generate-python-sbom.js
 */

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const REPO_ROOT = path.join(__dirname, '..');
const OUTPUT_PATH = path.join(REPO_ROOT, 'legal', 'PYTHON-BUNDLED-PACKAGES.md');
const REQUIREMENTS_FILES = [
  path.join(REPO_ROOT, 'requirements-windows-build.txt'),
  path.join(REPO_ROOT, 'requirements-macos-build.txt'),
];

function parsePinnedRequirements(filePath) {
  if (!fs.existsSync(filePath)) {
    return [];
  }

  const packages = [];
  const lines = fs.readFileSync(filePath, 'utf8').split(/\r?\n/);

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }

    const match = trimmed.match(/^([A-Za-z0-9][A-Za-z0-9._-]*)\s*==\s*([^;\s]+)/);
    if (match) {
      packages.push({ name: match[1], version: match[2], source: path.basename(filePath) });
    }
  }

  return packages;
}

function mergePackages(allPackages) {
  const byName = new Map();

  for (const pkg of allPackages) {
    const existing = byName.get(pkg.name);
    if (!existing) {
      byName.set(pkg.name, { ...pkg, sources: [pkg.source] });
      continue;
    }

    if (existing.version !== pkg.version) {
      existing.versionNote = `platform-specific (${existing.version} vs ${pkg.version})`;
    }

    if (!existing.sources.includes(pkg.source)) {
      existing.sources.push(pkg.source);
    }
  }

  return [...byName.values()].sort((a, b) => a.name.localeCompare(b.name));
}

function tryPipLicenseTable(packages) {
  const venvPython = path.join(REPO_ROOT, '.venv', 'Scripts', 'python.exe');
  const venvPythonUnix = path.join(REPO_ROOT, '.venv', 'bin', 'python');
  const python = fs.existsSync(venvPython)
    ? venvPython
    : (fs.existsSync(venvPythonUnix) ? venvPythonUnix : null);

  if (!python) {
    return null;
  }

  try {
    execSync(`"${python}" -m pip show pip-licenses`, { stdio: 'ignore' });
  } catch (error) {
    return null;
  }

  try {
    const output = execSync(
      `"${python}" -m pip_licenses --from=mixed --format=markdown --with-urls --with-license-file`,
      { encoding: 'utf8', maxBuffer: 10 * 1024 * 1024 }
    );
    return output.trim();
  } catch (error) {
    return null;
  }
}

function renderMarkdown(packages, pipLicensesMarkdown) {
  const generatedAt = new Date().toISOString();
  const lines = [
    '# Bundled Python packages (installer builds)',
    '',
    'Auto-generated by `node scripts/generate-python-sbom.js`. This lists **direct pins**',
    'from `requirements-*-build.txt` used when packaging AvaNevis. Transitive dependencies',
    'are installed by pip during `npm run prepare-build` but are not enumerated here.',
    '',
    `Generated: ${generatedAt}`,
    '',
    '| Package | Version | Build requirements file |',
    '|---------|---------|---------------------------|',
  ];

  for (const pkg of packages) {
    const version = pkg.versionNote || pkg.version;
    lines.push(`| ${pkg.name} | ${version} | ${pkg.sources.join(', ')} |`);
  }

  lines.push(
    '',
    '## Full license scan (optional)',
    '',
    'With a dev `.venv` and `pip install pip-licenses`, re-run this script to append',
    'a pip-reported license table. Otherwise verify licenses on [PyPI](https://pypi.org/)',
    'or in each package\'s installed `METADATA` before commercial distribution.',
    ''
  );

  if (pipLicensesMarkdown) {
    lines.push('## pip-licenses output (dev environment)', '', pipLicensesMarkdown, '');
  }

  return `${lines.join('\n')}\n`;
}

function main() {
  const allPackages = REQUIREMENTS_FILES.flatMap(parsePinnedRequirements);
  const merged = mergePackages(allPackages);
  const pipTable = tryPipLicenseTable(merged);

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, renderMarkdown(merged, pipTable), 'utf8');
  console.log(`Wrote ${path.relative(REPO_ROOT, OUTPUT_PATH)} (${merged.length} direct pins)`);
}

if (require.main === module) {
  main();
}

module.exports = {
  mergePackages,
  parsePinnedRequirements,
  renderMarkdown,
};
